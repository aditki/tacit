def _split_config(config: dict) -> tuple[dict, dict]:
    """Split config into YAML-safe settings and secrets (.env)."""
    secret_keys = {
        "grafana_api_key": "GRAFANA_API_KEY",
        "llm_api_key": "LLM_API_KEY",
        "slack_bot_token": "SLACK_BOT_TOKEN",
        "slack_app_token": "SLACK_APP_TOKEN",
        "slack_signing_secret": "SLACK_SIGNING_SECRET",
        "signalfx_api_token": "SIGNALFX_API_TOKEN",
        "llm_aws_access_key_id": "LLM_AWS_ACCESS_KEY_ID",
        "llm_aws_secret_access_key": "LLM_AWS_SECRET_ACCESS_KEY",
    }
    yaml_config: dict = {}
    secrets: dict = {}

    for k, v in config.items():
        if k in secret_keys:
            if v:
                secrets[secret_keys[k]] = v
        else:
            yaml_config[k] = v

    # Map non-secret config keys to env-style for the yaml
    structured: dict = {
        "grafana": {
            "url": yaml_config.pop("grafana_url", "http://localhost:3000"),
            "org_id": yaml_config.pop("grafana_org_id", 1),
        },
        "llm": {
            "provider": yaml_config.pop("llm_provider", "anthropic"),
            "model": yaml_config.pop("llm_model", "claude-sonnet-4-20250514"),
        },
    }
    api_base = yaml_config.pop("llm_api_base", "")
    if api_base:
        structured["llm"]["api_base"] = api_base

    # Bedrock config
    bedrock_region = yaml_config.pop("llm_bedrock_region", "")
    bedrock_role_arn = yaml_config.pop("llm_bedrock_role_arn", "")
    if structured["llm"].get("provider") == "bedrock" or bedrock_region:
        if bedrock_region:
            structured["llm"]["bedrock_region"] = bedrock_region
        if bedrock_role_arn:
            structured["llm"]["bedrock_role_arn"] = bedrock_role_arn

    # SignalFx config
    sfx_enabled = yaml_config.pop("signalfx_enabled", False)
    sfx_realm = yaml_config.pop("signalfx_realm", "")
    sfx_group = yaml_config.pop("signalfx_dashboard_group", "")
    if sfx_enabled or sfx_realm:
        structured["signalfx"] = {
            "enabled": sfx_enabled,
        }
        if sfx_realm:
            structured["signalfx"]["realm"] = sfx_realm
        if sfx_group:
            structured["signalfx"]["dashboard_group"] = sfx_group

    structured["log_level"] = yaml_config.pop("log_level", "INFO")

    # Carry over any remaining keys
    structured.update(yaml_config)
    return structured, secrets


# ── dashforge doctor ─────────────────────────────────────────────────────────
@cli.command()
def doctor():
    """Validate Grafana, datasources, LLM connectivity, and cache state."""
    _header("DashForge Doctor")
    _load_env()

    from concurrent.futures import ThreadPoolExecutor, as_completed

    # ── Synchronous local checks (instant) ────────────────────────────
    checks_passed = 0
    checks_total = 0
    fatal_failures: list[str] = []
    warnings: list[str] = []

    # 1. Config file
    checks_total += 1
    if CONFIG_FILE.exists():
        _success(f"Config found: {CONFIG_FILE}")
        checks_passed += 1
    else:
        _warn(f"No config at {CONFIG_FILE} — run `dashforge init`")
        fatal_failures.append("config")

    # 2. Archetypes (local file check, no network)
    checks_total += 1
    arch_ok = _check_archetypes()
    if arch_ok:
        checks_passed += 1
    else:
        warnings.append("archetypes")

    # 3. Cache dir
    checks_total += 1
    data_dir = Path("data")
    if data_dir.exists():
        _success(f"Data directory exists: {data_dir.resolve()}")
        checks_passed += 1
    else:
        _info("Data directory will be created on first run")
        checks_passed += 1  # Not a failure

    # ── Parallel network checks ───────────────────────────────────────
    # Run Grafana, LLM, and SignalFx checks concurrently to reduce wall time
    sfx_enabled = False
    try:
        from dashforge.config import settings as _sfx_settings

        sfx_enabled = _sfx_settings.signalfx_enabled and bool(_sfx_settings.signalfx_api_token)
    except Exception:
        pass

    network_checks: dict[str, Callable[[], bool]] = {
        "grafana": _check_grafana,
        "llm": _check_llm,
    }
    if sfx_enabled:
        network_checks["signalfx"] = _check_signalfx

    network_results: dict[str, bool] = {}
    with ThreadPoolExecutor(max_workers=len(network_checks)) as pool:
        futures: dict[Future[bool], str] = {pool.submit(fn): name for name, fn in network_checks.items()}
        for future in as_completed(futures):
            name = futures[future]
            try:
                network_results[name] = future.result()
            except Exception:
                network_results[name] = False

    grafana_ok = network_results.get("grafana", False)

    checks_total += len(network_checks)
    for name, ok in network_results.items():
        if ok:
            checks_passed += 1
        else:
            fatal_failures.append(name)

    # 4. Datasource discovery (depends on Grafana — sequential)
    if grafana_ok:
        checks_total += 1
        ds_ok = _check_datasources()
        if ds_ok:
            checks_passed += 1
        else:
            warnings.append("datasources")

    # ── Summary with classified failures ──────────────────────────────
    console.print()
    if checks_passed == checks_total:
        _success(f"All {checks_total} checks passed!")
    else:
        _warn(f"{checks_passed}/{checks_total} checks passed")
        if fatal_failures:
            _fail(f"Fatal: {', '.join(fatal_failures)}")
        if warnings:
            _warn(f"Warnings: {', '.join(warnings)}")
        if "grafana" in fatal_failures:
            _info("Run `dashforge connect grafana` to fix Grafana connection")
        if "signalfx" in fatal_failures:
            _info("Run `dashforge connect signalfx` to fix SignalFx connection")
        if "llm" in fatal_failures:
            _info("Check your LLM_API_KEY in ~/.dashforge/.env")


def _load_env():
    """Load ~/.dashforge/.env and point config to ~/.dashforge/config.yaml."""
    env_file = DASHFORGE_HOME / ".env"
    if env_file.exists():
        from dotenv import load_dotenv

        load_dotenv(env_file)

    if CONFIG_FILE.exists() and not os.environ.get("DASHFORGE_CONFIG"):
        os.environ["DASHFORGE_CONFIG"] = str(CONFIG_FILE)


def _check_grafana() -> bool:
    try:
        import httpx

        from dashforge.config import settings

        url = settings.grafana_url.rstrip("/")
        api_key = settings.grafana_api_key

        if not api_key:
            _warn("Grafana: no API key configured")
            return False

        headers = {"Authorization": f"Bearer {api_key}"}
        resp = httpx.get(f"{url}/api/org", headers=headers, timeout=10)
        if resp.status_code == 200:
            org = resp.json()
            _success(f"Grafana: connected to \"{org.get('name', 'unknown')}\" at {url}")
            return True
        else:
            _fail(f"Grafana: HTTP {resp.status_code} from {url}/api/org")
            return False
    except Exception as e:
        _fail(f"Grafana: {e}")
        return False


def _check_datasources() -> bool:
    try:
        import httpx

        from dashforge.config import settings

        url = settings.grafana_url.rstrip("/")
        headers = {"Authorization": f"Bearer {settings.grafana_api_key}"}
        resp = httpx.get(f"{url}/api/datasources", headers=headers, timeout=10)
        if resp.status_code == 200:
            ds_list = resp.json()
            types: dict[str, int] = {}
            for ds in ds_list:
                t = ds.get("type", "unknown")
                types[t] = types.get(t, 0) + 1
            summary = ", ".join(f"{v}× {k}" for k, v in sorted(types.items()))
            _success(f"Datasources: {len(ds_list)} found ({summary})")
            return True
        else:
            _fail(f"Datasources: HTTP {resp.status_code}")
            return False
    except Exception as e:
        _fail(f"Datasources: {e}")
        return False


def _check_llm() -> bool:
    try:
        from dashforge.config import settings

        provider = settings.llm_provider
        api_key = settings.llm_api_key
        model = settings.llm_model

        if not api_key and provider not in ("ollama", "bedrock"):
            _warn(f"LLM: no API key for {provider}")
            return False

        # Light validation — don't actually call the API
        if provider == "anthropic" and api_key.startswith("sk-ant-"):
            _success(f"LLM: {provider} / {model} (key looks valid)")
            return True
        elif provider in ("openai", "azure") and api_key.startswith("sk-"):
            _success(f"LLM: {provider} / {model} (key looks valid)")
            return True
        elif provider == "bedrock":
            try:
                import boto3

                session_kwargs: dict = {"region_name": settings.llm_bedrock_region}
                if settings.llm_aws_access_key_id:
                    session_kwargs["aws_access_key_id"] = settings.llm_aws_access_key_id
                    session_kwargs["aws_secret_access_key"] = settings.llm_aws_secret_access_key
                session = boto3.Session(**session_kwargs)
                # Mirror _build_boto3_session: assume role if configured
                if settings.llm_bedrock_role_arn:
                    sts = session.client("sts")
                    assumed = sts.assume_role(
                        RoleArn=settings.llm_bedrock_role_arn,
                        RoleSessionName="dashforge-bedrock",
                        DurationSeconds=3600,
                    )
                    creds = assumed["Credentials"]
                    session = boto3.Session(
                        aws_access_key_id=creds["AccessKeyId"],
                        aws_secret_access_key=creds["SecretAccessKey"],
                        aws_session_token=creds["SessionToken"],
                        region_name=settings.llm_bedrock_region,
                    )
                sts = session.client("sts")
                identity = sts.get_caller_identity()
                account = identity.get("Account", "?")
                model_id = settings.llm_bedrock_model_id or model
                _success(f"LLM: bedrock / {model_id} (AWS account {account}, region {settings.llm_bedrock_region})")
                return True
            except ImportError:
                _fail("LLM: bedrock requires boto3 — pip install 'dashforge[bedrock]'")
                return False
            except Exception as be:
                _fail(f"LLM: bedrock AWS auth failed — {be}")
                return False
        elif provider == "ollama":
            import httpx

            base = settings.llm_api_base or "http://localhost:11434"
            try:
                resp = httpx.get(f"{base}/api/tags", timeout=5)
                if resp.status_code == 200:
                    _success(f"LLM: ollama at {base} / {model}")
                    return True
            except Exception:
                pass
            _warn(f"LLM: ollama at {base} not reachable")
            return False
        else:
            _success(f"LLM: {provider} / {model} (key present)")
            return True
    except Exception as e:
        _fail(f"LLM: {e}")
        return False


def _check_archetypes() -> bool:
    try:
        from dashforge.archetypes.templates import ALL_ARCHETYPES

        total_panels = sum(len(a.panels) for a in ALL_ARCHETYPES)
        _success(f"Archetypes: {len(ALL_ARCHETYPES)} loaded, {total_panels} panels")
        return True
    except Exception as e:
        _fail(f"Archetypes: {e}")
        return False


def _check_signalfx() -> bool:
    try:
        import httpx

        from dashforge.config import settings

        realm = settings.signalfx_realm
        token = settings.signalfx_api_token
        if not token:
            _warn("SignalFx: no API token configured")
            return False

        resp = httpx.get(
            f"https://api.{realm}.signalfx.com/v2/metric",
            headers={"X-SF-TOKEN": token},
            params={"query": "*", "limit": 1},
            timeout=10,
        )
        if resp.status_code == 200:
            data = resp.json()
            count = data.get("count", "?")
            _success(f"SignalFx: connected to realm {realm} ({count} metrics)")
            return True
        elif resp.status_code == 401:
            _fail("SignalFx: authentication failed (HTTP 401) — check API token")
            return False
        else:
            _fail(f"SignalFx: HTTP {resp.status_code} from api.{realm}.signalfx.com")
            return False
    except Exception as e:
        _fail(f"SignalFx: {e}")
        return False


