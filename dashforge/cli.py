# ── dashforge init ───────────────────────────────────────────────────────────
@cli.command()
@click.option("--non-interactive", is_flag=True, help="Skip prompts, use defaults")
def init(non_interactive: bool):
    """Interactive setup wizard. Creates ~/.dashforge/config.yaml."""
    _header("DashForge Setup")

    if CONFIG_FILE.exists() and not non_interactive:
        if not _confirm("Config already exists. Overwrite?", default=False):
            _info("Keeping existing config.")
            return

    DASHFORGE_HOME.mkdir(parents=True, exist_ok=True)

    if non_interactive:
        config = _default_config()
        _info("Using default config (non-interactive mode)")
    else:
        config = _interactive_setup()

    # Write config (secrets go to .env, not YAML)
    config_data, secrets = _split_config(config)
    with open(CONFIG_FILE, "w") as f:
        f.write("# DashForge configuration — generated by `dashforge init`\n")
        f.write("# Secrets (API keys) are in ~/.dashforge/.env\n\n")
        yaml.dump(config_data, f, default_flow_style=False, sort_keys=False)

    env_path = DASHFORGE_HOME / ".env"
    with open(env_path, "w") as f:
        f.write("# DashForge secrets — generated by `dashforge init`\n")
        for k, v in secrets.items():
            f.write(f"{k}={v}\n")
    env_path.chmod(0o600)

    _success(f"Config written to {CONFIG_FILE}")
    _success(f"Secrets written to {env_path}")
    console.print()
    _info("Next steps:")
    _info("  dashforge doctor   — validate your setup")
    _info("  dashforge test     — run a sample investigation")
    _info("  dashforge serve    — start the API server")


