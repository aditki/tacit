@connect.command("grafana")
@click.option("--url", default=None, help="Grafana URL")
@click.option("--api-key", default=None, help="Grafana service account token")
def connect_grafana(url: str | None, api_key: str | None):
    """Test and persist Grafana connection."""
    _header("Connect to Grafana")
    _load_env()

    if url is None:
        url = _prompt("Grafana URL", "http://localhost:3000")
    if api_key is None:
        api_key = _prompt("API key (service account token)", "")

    if not api_key:
        _fail("API key is required")
        return

    # Test connection
    import httpx

    try:
        headers = {"Authorization": f"Bearer {api_key}"}
        resp = httpx.get(f"{url.rstrip('/')}/api/org", headers=headers, timeout=10)
        if resp.status_code != 200:
            _fail(f"Connection failed: HTTP {resp.status_code}")
            return
        org = resp.json()
        _success(f"Connected to \"{org.get('name', 'unknown')}\" at {url}")
    except Exception as e:
        _fail(f"Connection failed: {e}")
        return

    # Discover datasources
    try:
        resp = httpx.get(f"{url.rstrip('/')}/api/datasources", headers=headers, timeout=10)
        if resp.status_code == 200:
            ds_list = resp.json()
            _success(f"Found {len(ds_list)} datasources:")
            for ds in ds_list:
                _info(f"  {ds['name']} ({ds['type']})")
    except Exception:
        pass

    # Persist
    if _confirm("Save this connection?", default=True):
        _ensure_config()
        _update_config({"grafana": {"url": url}})
        _update_env({"GRAFANA_API_KEY": api_key})
        _success("Connection saved!")
        _info("Run `dashforge doctor` to verify full setup")


